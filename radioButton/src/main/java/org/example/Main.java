public class Main {
    public static void main(String[] args) {

        WebDriverManager.chromedriver().setup();
        WebDriver driver = new ChromeDriver();
        driver.manage().window().maximize();
        driver.manage().timeouts().implicitlyWait(Duration.ofSeconds(10));

        String baseUrl= "https://demo.guru99.com/test/radio.html";

        driver.get(baseUrl);

        WebElement r1 = driver.findElement(By.id("vfb-7-1"));
        WebElement r2 = driver.findElement(By.id("vfb-7-2"));
        WebElement r3 = driver.findElement(By.id("vfb-7-3"));

        r2.click();

        if(r1.isSelected()){

            System.out.println("Tested");
        }
        else
        {
            System.out.println("Faied!");
        }

        driver.quit();













    }
}































































































































































































































































































































































































































































































































































































